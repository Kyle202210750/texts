public class MathRushedProblem11 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // get -3 ^ -2
        double num1 = Math.pow(-3.0, -2.0);
        // Calculate the num1 and raised to the power of 2
        double result = Math.pow(num1, 2.0);
        
        System.out.println(result);
        // Output: 0.012345679012345678
    }
}
